void glutBitmapCharacter(void *font, int character)
{
    GLint para[16];
    int i;

    glGetIntegerv(GL_MODELVIEW_MATRIX, (GLint*)&para);

    if (glfont_was_init == 0)
    {
	glfont_was_init = 1;
	makeRasterFont();
    }

    for (i = 0; i < 16; i++)
	lastpara[i] = para[i];

    wasstroke = 0;

    glPushAttrib (GL_LIST_BIT);
    glListBase(fontOffset);
    glCallLists(1, GL_UNSIGNED_BYTE, (GLubyte *) &character);
    glPopAttrib ();
}

void glutStrokeCharacter(void * font, int character)
{
    GLint para[16];
    int i = 0;
    bool newcall = 0;

    glGetIntegerv(GL_MODELVIEW_MATRIX, (GLint*)&para);

    for (i = 0; i < 16; i++)
    {
	if (para[i] != lastpara[i])
	    newcall = 1;

	lastpara[i] = para[i];
    }

    if (newcall || wasstroke == 0)
	glRasterPos2f(0,0);

    glutBitmapCharacter(font,character);

    wasstroke = 1;
}



